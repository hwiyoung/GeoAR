package com.google.ar.core.examples.java.common;

public class AverageInfo {
    private float x_avereage;
    private float y_avereage;
    private float z_avereage;

    public AverageInfo(float x_avereage, float y_avereage, float z_avereage) {
        this.x_avereage = x_avereage;
        this.y_avereage = y_avereage;
        this.z_avereage = z_avereage;
    }

    public float getX_avereage() {
        return x_avereage;
    }

    public float getY_avereage() {
        return y_avereage;
    }

    public float getZ_avereage() {
        return z_avereage;
    }
}
